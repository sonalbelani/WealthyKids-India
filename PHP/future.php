    <p><b>As a parent, your kids are the most important part of your lives.You would do anything to make your children happy and to secure their life & future. Smallest of your happy moments depend on them. But sometimes managing spending and saving becomes a tricky task ,  so there are many Child Investment Schemes to address your child's future needs, even in your absence.</p></b>
<br> <h2 style="color:#3498DB;"><b>WHAT IS A CHILD PLAN?</h2></b>
        <p>A child plan is a investment cum insurance option to meet the financial needs of a child. In a child plan, there are two components– insurance and investment.

            The insurance component is designed to protect the child from unfortunate events such as the demise of the parent wherein the child gets a fixed annual payment in case such an event occurs.
            
            The investment component is designed to meet the financial needs of the child through accumulation of money by investing in various instruments.</p>
          <br>
        <h2 style="color:#3498DB;"><b>WHY BUY A CHILD'S PLAN?</h2></b><br>
            <p>
               <b> 1. Protection against uncertain times :</b></p><br> <p><img src="https://www.aegonlife.com/insurance-investment-knowledge/wp-content/uploads/2016/09/Why-is-a-child-plan-absolutely-necessary-for-your-kid%E2%80%99s-future-768x508.jpg " style="float:right; width="300" height="300"></p>
<p>Child plans come with insurance benefit wherein if a parent dies, the child will get the maturity amount at the end of the policy term. Along with this, the child will also get annual payments every year from the year of demise to sustain regular life.
 <br> <br>

<b>2. Lump-sum benefit: :</b> <br> <br>

 Plans provides your children with a lump-sum benefit in the case of your death withinthe policy term. <br> <br>
<p>
<b>3. Waiver of premium: : </b><br><br>
Many child plans offer benefits like a waiver of premium. This ensures that future premium payments do not need to be paid in case an unfortunate event occurs. The investment continues to grow without the need to pay premiums, and provides a lump sum payout at maturity. This ensures that your child’s dreams are secured, no matter what. <br> <br><p><img src="https://www.oneinsure.com/Media/Default/BlogImages/child-future.jpg " style="float:right; width="300" height="300"></p>
<p style="text-align:left;">

<b>4. Collateral security  : </b><br><br>

The child plan is accepted as a valid security against loans. In case you need emergency funds in the future , you can use the plan to raise funds. <br> <br>
 
<b>5. Tax Benefits : </b><br><br>

 Some Plans offer tax benefits to the policyholder under section 80C of the Income Tax Act.


